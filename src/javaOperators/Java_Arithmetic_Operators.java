package javaOperators;



public class Java_Arithmetic_Operators {

	

	public static void main(String[] args) {
		int a=10;
		int b=5;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);  // also called modules and gives us reminder after divsion.
	}

}
