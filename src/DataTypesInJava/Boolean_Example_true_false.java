package DataTypesInJava;

public class Boolean_Example_true_false {

	public static void main(String[] args) {
		 // Test true and false booleans.
        boolean Tajamul = true;
        if (Tajamul) {
            System.out.println("A");
        }
        Tajamul = false;
        if (!Tajamul) {
            System.out.println("B");
        }
    }
}